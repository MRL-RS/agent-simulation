package mrl.partitioning.voronoi;

/**
 * @author Vahid Hooshangi
 */
public class MyPoint {
    long x;
    long y;

    public MyPoint(long x, long y) {
        this.x = x;
        this.y = y;
    }

    public long getX() {
        return x;
    }

    public long getY() {
        return y;
    }
}
